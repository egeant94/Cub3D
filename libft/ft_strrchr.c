#include "libft.h"

char	*ft_strrchr(char const *str, int c)
{
	int i;

	if (str == 0)
		return (0);
	i = ft_strlen(str);
	while (i >= 0)
	{
		if (str[i] == (char)c)
			return ((char *)str + i);
		i--;
	}
	return (0);
}
